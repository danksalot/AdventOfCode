from copy import deepcopy
def add(a,b):
	return reduce([a,b])
def reduce(x):
	x = deepcopy(x)
	cont = True
	while cont:
		cont = False
		stack = [(0,x,[])]
		while stack:
			n,v,p = stack.pop()
			if not isinstance(v, list):
				continue
			if n >= 4:
				for a, ix in p:
					if ix > 0:
						while isinstance(a[ix-1], list):
							a = a[ix-1]
							ix = len(a)
						a[ix-1] += v[0]
						break
				for a, ix in p:
					if ix < len(a)-1:
						while isinstance(a[ix+1], list):
							a = a[ix+1]
							ix = -1
						a[ix+1] += v[1]
						break
				p[0][0][p[0][1]] = 0
				cont = True
				break
			else:
				for i,sub in list(enumerate(v))[::-1]:
					print('Explode - Adding to stack:', (n+1,sub,[[v,i]]+p))
					stack.append((n+1,sub,[[v,i]]+p))
		if cont:			
			continue

		stack = [(0,x,None,None)]
		while stack:
			n,v,p,ix = stack.pop()
			if not isinstance(v, list):
				if v >= 10:
					p[ix] = [v // 2, (v+1) // 2]
					cont = True
					break
			else:
				for i,sub in list(enumerate(v))[::-1]:
					print('Split - Adding to stack:', (n+1,sub,v,i))
					stack.append((n+1,sub,v,i))
	return x

